function VideoItem({ src }: { src: string }) {
  const ref = useRef<HTMLVideoElement>(null);
  const [isMuted, setIsMuted] = useState(false);

  const tryPlayWithSound = async () => {
    const el = ref.current;
    if (!el) return;
    el.muted = false;
    el.volume = 1;
    el.currentTime = 0;
    try {
      await el.play();
      setIsMuted(false);
    } catch {
      // Autoplay with sound blocked: fallback to muted autoplay
      el.muted = true;
      setIsMuted(true);
      try { await el.play(); } catch {}
    }
  };

  const pauseAndReset = () => {
    const el = ref.current;
    if (!el) return;
    el.pause();
    el.currentTime = 0;
  };

  const toggleMute = async () => {
    const el = ref.current;
    if (!el) return;
    // If paused, prefer playing with sound; fallback to muted if blocked
    if (el.paused) {
      el.muted = false;
      try {
        await el.play();
        setIsMuted(false);
        return;
      } catch {}
      el.muted = true;
      setIsMuted(true);
      try { await el.play(); } catch {}
      return;
    }
    // If playing, toggle mute without pausing
    if (el.muted) {
      el.muted = false;
      setIsMuted(false);
      try { await el.play(); } catch {}
    } else {
      el.muted = true;
      setIsMuted(true);
      try { await el.play(); } catch {}
    }
  };

  const unmuteOnClick = async () => {
    const el = ref.current;
    if (!el) return;
    if (el.muted) {
      el.muted = false;
      setIsMuted(false);
      try { await el.play(); } catch {}
    }
  };

  return (
    <div className="mb-4 rounded-xl overflow-hidden border bg-black/5 dark:bg-white/5 break-inside-avoid relative">
      <video
        ref={ref}
        src={encodeURI(src)}
        playsInline
        loop
        preload="metadata"
        muted={isMuted}
        tabIndex={0}
        className="w-full h-auto block transition-transform duration-200 hover:scale-[1.01]"
        onMouseEnter={tryPlayWithSound}
        onMouseLeave={pauseAndReset}
        onTouchStart={toggleMute}
        onClick={toggleMute}
        onKeyDown={(e) => { if (e.key === 'Tab') { void toggleMute(); } }}
      />
      {isMuted && (
        <button
          onClick={unmuteOnClick}
          className="absolute inset-x-0 bottom-3 mx-auto w-fit px-3 py-1.5 text-xs font-medium rounded-full bg-black/60 text-white backdrop-blur hover:bg-black/70"
        >
          Tap to unmute
        </button>
      )}
    </div>
  );
}

